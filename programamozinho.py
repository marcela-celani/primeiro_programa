operacao = "+" #digite um operador entre +, -, * ou /
numero1 = 2 #digite um numero
numero2 = 3 #digite um numero

if operacao == "+":
	print(numero1+numero2)

elif operacao == "-":
	print(numero1-numero2)

elif operacao == "*":
	print(numero1*numero2)

elif operacao == "/":
	print(numero1/numero2)